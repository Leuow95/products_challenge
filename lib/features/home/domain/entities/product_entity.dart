class ProductEntity {
  final String title;
  final String type;
  final String description;
  final String filename;
  final int height;
  final int widht;
  final double price;
  final int rating;

  ProductEntity({
    required this.title,
    required this.type,
    required this.description,
    required this.filename,
    required this.height,
    required this.widht,
    required this.price,
    required this.rating,
  });
}
